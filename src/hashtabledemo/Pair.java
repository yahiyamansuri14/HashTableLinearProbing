
package hashtabledemo;

/**
 *
 * @author Yahiya Mansuri
 */
public class Pair {
    private int key;
	private String value;

	public Pair(int key, String value){
		this.key = key;
		this.value = value;
	}

	public int getKey(){
		return key;
	}

	public String getValue(){
		return value;
	}

}


